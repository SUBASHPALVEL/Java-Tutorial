public class Assignment_operator {
    public static void main(String[] args) {
 
        int numA=11;

        System.out.println("numA="+numA);
        System.out.println(numA+"+=2 equals to "+(numA+=2));

        System.out.println("numA="+numA);
        System.out.println(numA+"-=2 equals to "+(numA-=2));

        System.out.println("numA="+numA);
        System.out.println(numA+"*=2 equals to "+(numA*=2));

        System.out.println("numA="+numA);
        System.out.println(numA+"/=2 equals to "+(numA/=2));

        System.out.println("numA="+numA);
        System.out.println(numA+"%=2 equals to "+(numA%=2));

        System.out.println("numA="+numA);
        System.out.println(numA+"&=2 equals to "+(numA&=2));

        System.out.println("numA="+numA);
        System.out.println(numA+"|=2 equals to "+(numA|=2));

        System.out.println("numA="+numA);
        System.out.println(numA+">>=2 equals to "+(numA>>=2));

        System.out.println("numA="+numA);
        System.out.println(numA+"<<=2 equals to "+(numA<<=2));

        System.out.println("numA="+numA);
        System.out.println(numA+">>>=2 equals to "+(numA>>>=2));

    }
}
