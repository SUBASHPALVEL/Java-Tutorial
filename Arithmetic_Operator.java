public class Arithmetic_Operator{
    public static void main(String[] args) {


        // ASSIGNMENTS
        int numA = 5;
        int numB = 10;

        // OPERATIONS
        int addition = numA + numB;
        System.out.println("numA + numB = " + addition);

        int subtraction = numA - numB;
        System.out.println("numA - numB =" + subtraction);

        int multiplication = numA * numB;
        System.out.println("numA * numB =" + multiplication);

        int division = numB / numA;
        System.out.println("numB / numA = " + division);

        int modulus = numB % numA;
        System.out.println("numB % numA =" + modulus);

    }
}