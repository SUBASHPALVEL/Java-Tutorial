public class Scaling_in_numbers {
    public static void main(String[] args) {

        // E means 10 to the power of
        // P means 2 to the power of

        double myScaledBigNumber = 1.0E6;
        System.out.println("myScaledBigNumber = "+myScaledBigNumber);

        double mySclaedSmallNumber =1.0E-3;
        System.out.println("mySclaedSmallNumber = "+mySclaedSmallNumber);

        double myExponentBigNumber= 0xFF.0P15;
        System.out.println("myExponentBigNumber = "+myExponentBigNumber);

        double myExponentSmallNumber=0xFF.0P-15;
        System.out.println("myExponentSmallNumber = "+myExponentSmallNumber);

    }
}
