package WrapperClass;

public class Autoboxing {
    /*
    Autoboxing and unboxing are features in Java
    It allow you to automatically convert between primitive data types (such as int, double, char, etc.) 
    and their corresponding wrapper classes (e.g., Integer, Double, Character) 
    when working with objects and collections that expect objects. 

     */
    

     /*
      Autoboxing: 
      Autoboxing is the automatic conversion of a primitive data type to its corresponding wrapper class object.
      This happens when you assign a primitive value to a wrapper class variable or
      when you pass a primitive value to a method that expects an object of the wrapper class.
      */


      public static void main(String[] args) {
        Integer integerObj = 42; // Autoboxing, int to Integer
        Double doubleObj = 3.14; // Autoboxing, double to Double

      }
}
